// Test
let testRegExp = /s/;
testRegExp.test("string");
// Exec
let execRegExp = /s/;
execRegExp.exec("string");
// Match
let matchRegExp = /s/;
"string".match(matchRegExp);
// Search
let searchRegExp = /s/;
"string".search(searchRegExp);
// Replace
let replaceRegExp = /s/;
"string".replace(replaceRegExp, "S");
// Split
let splitRegExp = /s/;
"string".split(splitRegExp);
